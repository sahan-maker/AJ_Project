    def _freq_axis(self):
        eng = self.engine
        return np.linspace(eng.span_start, eng.span_end, self.N_FFT)

    def _set_xlim(self):
        eng = self.engine
        self.ax_fft.set_xlim(eng.span_start, eng.span_end)
        xticks = [c["freq"] for c in eng.channels]
        self.ax_fft.set_xticks(xticks)
        self.ax_fft.set_xticklabels([f"{x:.1f}" for x in xticks], fontsize=6.5, color=MUTED, rotation=30)

    def _compute_fft_frame(self, ch_idx: int, iq_samples: np.ndarray) -> np.ndarray:
        """
        Build FFT display frame from actual IQ samples.
        Computes real FFT of the IQ, maps it to the display frequency axis.
        """
        eng     = self.engine
        freq    = self._freq_axis()
        ch_freq = eng.channels[ch_idx]["freq"]

        # Compute real FFT power from actual IQ samples
        n     = len(iq_samples)
        win   = np.hanning(n)
        fft   = np.fft.fft(iq_samples * win, n=n)
        psd   = np.abs(np.fft.fftshift(fft)) ** 2
        psd   = psd / (n * np.sum(win**2) + 1e-12)
        psd_db = 10 * np.log10(psd + 1e-12) + self.PEAK_POWER + 15   # shift to dBm range

        # Map IQ FFT bins to display frequency axis
        iq_freqs = np.linspace(ch_freq - eng.channel_bw*0.5,
                               ch_freq + eng.channel_bw*0.5, n)
        # Interpolate IQ FFT onto display freq axis
        frame = np.interp(freq, iq_freqs, psd_db,
                          left=self.NOISE_FLOOR, right=self.NOISE_FLOOR)

        # Add noise floor variation to out-of-channel bins
        noise_mask = (freq < ch_freq - eng.channel_bw*0.55) | (freq > ch_freq + eng.channel_bw*0.55)
        frame[noise_mask] = self.NOISE_FLOOR + np.random.randn(np.sum(noise_mask)) * 1.8

        from scipy.ndimage import uniform_filter1d
        frame = uniform_filter1d(frame, size=3)
        return frame

    def update_hop(self, hop_idx: int):
        if hop_idx < 0:
            return
        eng    = self.engine
        ch_idx = eng.sequence[hop_idx % len(eng.sequence)]
        ch     = eng.channels[ch_idx]
        freq   = self._freq_axis()

        # Generate REAL IQ samples — these power both the FFT display and the demod console
        iq_samples = ModulationEngine.generate(
            eng.modulation, n_samples=512, fs=1.0, snr_db=eng.snr_db
        )

        # Emit IQ to demod console (before FFT processing)
        self.iq_generated.emit(hop_idx, ch_idx, iq_samples)

        # Build FFT frame from real IQ
        frame = self._compute_fft_frame(ch_idx, iq_samples)

        self._peak_hold = np.maximum(self._peak_hold, frame)
        self._peak_hold = self._peak_hold * 0.97 + self.NOISE_FLOOR * 0.03

        self._fft_line.set_ydata(frame)
        self._peak_line.set_ydata(self._peak_hold)

        x0 = ch["freq"] - eng.channel_bw*0.5
        x1 = ch["freq"] + eng.channel_bw*0.5
        self._active_span.remove()
        self._active_span = self.ax_fft.axvspan(x0, x1, alpha=0.25, color=ch["color"], zorder=2)

        self._mod_text.set_text(f"MOD: {eng.modulation}")
        self._ch_text.set_text(f"CH:  {ch['label']}  {ch['freq']:.2f} MHz")
        self._snr_text.set_text(f"SNR: {eng.snr_db:.0f} dB")

        self._wf_data = np.roll(self._wf_data, -1, axis=0)
        self._wf_data[-1, :] = frame
        self._wf_im.set_data(self._wf_data)
        self._wf_im.set_extent([freq[0], freq[-1], self.WATERFALL_H, 0])
        self.draw_idle()

    def rebuild(self):
        self._wf_data   = np.full((self.WATERFALL_H, self.N_FFT), self.NOISE_FLOOR, dtype=float)
        self._peak_hold = np.full(self.N_FFT, self.NOISE_FLOOR)
        self._build_axes()
        self._init_artists()



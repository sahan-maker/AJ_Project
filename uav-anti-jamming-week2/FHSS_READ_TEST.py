# ─────────────────────────────────────────────────────────────────────────────
# Demodulator — extracts real decisions from IQ samples
# ─────────────────────────────────────────────────────────────────────────────
class Demodulator:
    """
    Performs actual demodulation on complex IQ samples generated by ModulationEngine.
    Returns structured DemodResult with real symbol/bit decisions and measured metrics.
    """

    @staticmethod
    def measure_snr(iq: np.ndarray) -> float:
        """
        Estimate SNR from IQ samples using moment-based method.
        Assumes signal has been normalized to unit power.
        SNR_est = E[|x|^2]^2 / Var(|x|^2)  (4th-order moment estimator)
        """
        mag2 = np.abs(iq) ** 2
        mu   = np.mean(mag2)
        var  = np.var(mag2)
        if var < 1e-12:
            return 40.0
        snr_lin = mu**2 / (var + 1e-12)
        return float(np.clip(10 * np.log10(snr_lin + 1e-12), -10, 50))

    @staticmethod
    def measure_evm(iq: np.ndarray, mod: str) -> float:
        """
        Error Vector Magnitude — difference between received symbols and ideal constellation.
        Returns EVM in percent.
        """
        ideal = Demodulator._get_ideal_constellation(iq, mod)
        if ideal is None or len(ideal) == 0:
            return 0.0
        errors = iq - ideal
        signal_power = np.mean(np.abs(ideal) ** 2) + 1e-12
        error_power  = np.mean(np.abs(errors) ** 2)
        evm_pct = 100.0 * np.sqrt(error_power / signal_power)
        return float(np.clip(evm_pct, 0, 100))

    @staticmethod
    def _get_ideal_constellation(iq: np.ndarray, mod: str) -> np.ndarray:
        """Map each IQ sample to its nearest ideal constellation point."""
        if mod == "BPSK":
            pts = np.array([1+0j, -1+0j])
        elif mod == "QPSK":
            pts = np.exp(1j * np.array([0.25, 0.75, 1.25, 1.75]) * np.pi)
        elif mod == "8PSK":
            pts = np.exp(1j * 2 * np.pi * np.arange(8) / 8)
        elif mod == "16QAM":
            r   = np.array([-3, -1, 1, 3]) / 3.0
            pts = np.array([a + 1j*b for a in r for b in r])
        elif mod == "64QAM":
            r   = (np.arange(8) * 2 - 7) / 7.0
            pts = np.array([a + 1j*b for a in r for b in r])
        else:
            return None

        # Find closest constellation point for each sample
        iq_flat = iq.flatten()[:min(len(iq), 512)]
        dists   = np.abs(iq_flat[:, None] - pts[None, :])
        nearest = pts[np.argmin(dists, axis=1)]
        return nearest

    @staticmethod
    def demodulate_psk(iq: np.ndarray, M: int) -> dict:
        """Decision-directed PSK demodulation. Returns symbol indices and bit string."""
        phases = np.angle(iq)
        # Normalize phases to [0, 2π)
        phases = phases % (2 * np.pi)
        sym_indices = np.round(phases / (2 * np.pi / M)).astype(int) % M

        k    = int(np.log2(M))
        bits = []
        for idx in sym_indices:
            # Gray code decode
            gray_idx = idx ^ (idx >> 1)
            bits.extend([(gray_idx >> (k - 1 - b)) & 1 for b in range(k)])

        return {
            "symbols": sym_indices[:16].tolist(),
            "bits":    bits[:64],
            "k_bps":   k,
        }

    @staticmethod
    def demodulate_qam(iq: np.ndarray, M: int) -> dict:
        """Decision-directed QAM demodulation."""
        sqM    = int(np.sqrt(M))
        levels = np.linspace(-(sqM - 1), sqM - 1, sqM) / (sqM - 1)
        k      = int(np.log2(M))

        I_dec = np.array([levels[np.argmin(np.abs(levels - s.real))] for s in iq])
        Q_dec = np.array([levels[np.argmin(np.abs(levels - s.imag))] for s in iq])

        I_idx = np.array([np.argmin(np.abs(levels - s.real)) for s in iq])
        Q_idx = np.array([np.argmin(np.abs(levels - s.imag)) for s in iq])
        sym_indices = I_idx * sqM + Q_idx

        bits = []
        for idx in sym_indices[:32]:
            for b in range(k):
                bits.append((idx >> (k - 1 - b)) & 1)

        return {
            "symbols":   sym_indices[:16].tolist(),
            "I_levels":  I_dec[:8].tolist(),
            "Q_levels":  Q_dec[:8].tolist(),
            "bits":      bits[:64],
            "k_bps":     k,
        }

    @staticmethod
    def demodulate_fsk(iq: np.ndarray, fs: float = 1.0) -> dict:
        """Non-coherent FSK: instantaneous frequency deviation → bit decisions."""
        phase = np.unwrap(np.angle(iq))
        inst_freq = np.diff(phase) / (2 * np.pi)          # normalized frequency
        bits = (inst_freq > 0).astype(int)

        # Measure deviation
        f0 = float(np.mean(inst_freq[bits == 0]))
        f1 = float(np.mean(inst_freq[bits == 1]))
        deviation = abs(f1 - f0)

        return {
            "bits":           bits[:64].tolist(),
            "freq_deviation": deviation,
            "f0_norm":        round(f0, 4),
            "f1_norm":        round(f1, 4),
        }

    @staticmethod
    def demodulate_msk_gmsk(iq: np.ndarray) -> dict:
        """Differential phase demodulation for MSK/GMSK (h=0.5)."""
        phase = np.unwrap(np.angle(iq))
        diff_phase = np.diff(phase)
        bits = (diff_phase > 0).astype(int)
        avg_step = float(np.mean(np.abs(diff_phase)))
        return {
            "bits":      bits[:64].tolist(),
            "avg_phase_step_rad": round(avg_step, 4),
            "h_measured": round(avg_step / np.pi, 4),
        }

    @staticmethod
    def demodulate_fm(iq: np.ndarray) -> dict:
        """FM discriminator: instantaneous frequency → audio samples."""
        phase    = np.unwrap(np.angle(iq))
        audio    = np.diff(phase)                          # discriminator output
        peak_dev = float(np.max(np.abs(audio)))
        thd      = Demodulator._thd(audio)
        return {
            "audio_samples":  [round(float(a), 4) for a in audio[:16]],
            "peak_deviation": round(peak_dev, 4),
            "thd_pct":        round(thd, 2),
        }

    @staticmethod
    def demodulate_am(iq: np.ndarray) -> dict:
        """AM envelope detection."""
        envelope    = np.abs(iq)
        dc          = float(np.mean(envelope))
        ac          = envelope - dc
        mod_index   = float(np.max(np.abs(ac)) / (dc + 1e-12))
        return {
            "dc_level":         round(dc, 4),
            "modulation_index": round(mod_index, 4),
            "peak_envelope":    round(float(np.max(envelope)), 4),
        }

    @staticmethod
    def demodulate_ofdm(iq: np.ndarray, n_sc: int = 64) -> dict:
        """OFDM: FFT per block, measure subcarrier powers."""
        blocks = []
        for start in range(0, len(iq) - n_sc, n_sc):
            block = iq[start:start + n_sc]
            blocks.append(np.fft.fft(block))
        if not blocks:
            return {"subcarrier_powers_db": [], "active_subcarriers": 0}

        sc_pwr = np.mean([np.abs(b)**2 for b in blocks], axis=0)
        threshold = np.max(sc_pwr) * 0.1
        active    = int(np.sum(sc_pwr > threshold))
        pwr_db    = [round(float(10 * np.log10(p + 1e-12)), 1) for p in sc_pwr[:16]]
        return {
            "subcarrier_powers_db": pwr_db,
            "active_subcarriers":   active,
            "n_fft":                n_sc,
        }

    @staticmethod
    def demodulate_css(iq: np.ndarray) -> dict:
        """LoRa-like CSS: dechirp and FFT to find symbol frequency bin."""
        n = len(iq)
        t = np.arange(n)
        # Reference down-chirp
        ref_chirp = np.exp(-1j * np.pi * 0.5 * (t / n) ** 2 * n)
        dechirped  = iq * ref_chirp
        spectrum   = np.abs(np.fft.fft(dechirped))
        peak_bin   = int(np.argmax(spectrum))
        peak_pwr   = float(np.max(spectrum))
        noise_floor = float(np.mean(spectrum)) + 1e-12
        sir         = round(20 * np.log10(peak_pwr / noise_floor), 1)
        return {
            "peak_bin":  peak_bin,
            "sir_db":    sir,
            "spreading_factor_est": round(np.log2(n), 1),
        }

    @staticmethod
    def demodulate_dsss(iq: np.ndarray, chip_rate: int = 11) -> dict:
        """DSSS: despread with reference PN, extract data bits."""
        rng     = np.random.default_rng(42)            # same seed used in ModulationEngine
        chips   = rng.integers(0, 2, len(iq)) * 2 - 1
        despread = (iq.real * chips)                    # correlate with PN
        n_bits  = len(iq) // chip_rate
        bits    = []
        for i in range(n_bits):
            segment = despread[i * chip_rate:(i + 1) * chip_rate]
            bits.append(1 if np.sum(segment) > 0 else 0)
        corr_gain = round(10 * np.log10(chip_rate), 1)
        return {
            "bits":              bits[:32],
            "chip_rate":         chip_rate,
            "processing_gain_db": corr_gain,
        }

    @staticmethod
    def demodulate_cw(iq: np.ndarray) -> dict:
        """CW: envelope thresholding → Morse dit/dah classification."""
        envelope  = np.abs(iq)
        threshold = (np.max(envelope) + np.min(envelope)) / 2
        key       = (envelope > threshold).astype(int)
        # Find runs
        runs = []
        current = key[0]
        count   = 1
        for v in key[1:]:
            if v == current:
                count += 1
            else:
                runs.append((int(current), count))
                current = v
                count   = 1
        runs.append((int(current), count))
        avg_on = float(np.mean([r[1] for r in runs if r[0] == 1])) if any(r[0] == 1 for r in runs) else 0
        elements = []
        for v, c in runs[:10]:
            if v == 1:
                elements.append("·" if c < avg_on * 1.5 else "—")
            else:
                elements.append("_")
        return {
            "elements":  elements,
            "duty_cycle": round(float(np.mean(key)), 3),
            "runs":      len(runs),
        }

    @staticmethod
    def _thd(signal: np.ndarray) -> float:
        """Total Harmonic Distortion estimate (%)."""
        spectrum = np.abs(np.fft.rfft(signal))
        if len(spectrum) < 2 or spectrum[1] < 1e-12:
            return 0.0
        fundamental = spectrum[1]
        harmonics   = np.sqrt(np.sum(spectrum[2:min(10, len(spectrum))]**2))
        return float(100.0 * harmonics / (fundamental + 1e-12))

    @classmethod
    def demodulate(cls, mod: str, iq: np.ndarray, snr_db: float) -> dict:
        """
        Top-level dispatcher. Returns a structured result dict with:
          - mod:         modulation name
          - snr_measured: estimated SNR from IQ samples (dB)
          - evm_pct:     EVM for constellation mods (%)
          - noise_ok:    True if measured SNR is within 3 dB of configured SNR
          - detail:      modulation-specific demod data
          - bits_sample: first 16 bits as hex string (when available)
        """
        # Normalise
        iq = iq / (np.max(np.abs(iq)) + 1e-12)

        snr_meas = cls.measure_snr(iq)
        noise_ok = (snr_meas >= snr_db - 4.0)

        evm = 0.0
        detail = {}

        if mod == "BPSK":
            detail = cls.demodulate_psk(iq, 2)
            evm    = cls.measure_evm(iq, mod)
        elif mod == "QPSK":
            detail = cls.demodulate_psk(iq, 4)
            evm    = cls.measure_evm(iq, mod)
        elif mod == "8PSK":
            detail = cls.demodulate_psk(iq, 8)
            evm    = cls.measure_evm(iq, mod)
        elif mod == "16QAM":
            detail = cls.demodulate_qam(iq, 16)
            evm    = cls.measure_evm(iq, mod)
        elif mod == "64QAM":
            detail = cls.demodulate_qam(iq, 64)
            evm    = cls.measure_evm(iq, mod)
        elif mod in ("RTCM/MSK",):
            detail = cls.demodulate_msk_gmsk(iq)
        elif mod == "GMSK":
            detail = cls.demodulate_msk_gmsk(iq)
        elif mod == "FSK":
            detail = cls.demodulate_fsk(iq)
        elif mod == "AM-DSB":
            detail = cls.demodulate_am(iq)
        elif mod == "FM":
            detail = cls.demodulate_fm(iq)
        elif mod == "OFDM":
            detail = cls.demodulate_ofdm(iq)
        elif mod == "CSS (LoRa-like)":
            detail = cls.demodulate_css(iq)
        elif mod == "DSSS":
            detail = cls.demodulate_dsss(iq)
        elif mod == "CW":
            detail = cls.demodulate_cw(iq)

        # Extract bits sample if present
        raw_bits = detail.get("bits", [])
        bits_hex = ""
        if raw_bits:
            # pack bits into bytes
            padded = raw_bits + [0] * (8 - len(raw_bits) % 8) if len(raw_bits) % 8 else raw_bits
            byte_vals = [
                int("".join(str(b) for b in padded[i:i+8]), 2)
                for i in range(0, min(len(padded), 64), 8)
            ]
            bits_hex = " ".join(f"{b:02X}" for b in byte_vals[:8])

        return {
            "mod":          mod,
            "snr_measured": round(snr_meas, 1),
            "snr_config":   round(snr_db, 1),
            "evm_pct":      round(evm, 1),
            "noise_ok":     noise_ok,
            "detail":       detail,
            "bits_hex":     bits_hex,
        }


# ─────────────────────────────────────────────────────────────────────────────
# Modulation Signal Generators
# ─────────────────────────────────────────────────────────────────────────────
class ModulationEngine:
    MODULATIONS = [
        "BPSK","QPSK","8PSK","16QAM","64QAM",
        "RTCM/MSK","GMSK","FSK","AM-DSB","FM",
        "OFDM","CSS (LoRa-like)","DSSS","CW"
    ]

    @staticmethod
    def generate(mod: str, n_samples: int, fs: float, snr_db: float = 20.0) -> np.ndarray:
        t   = np.arange(n_samples) / fs
        rng = np.random.default_rng()

        noise_power = 10 ** (-snr_db / 10)
        noise = (rng.standard_normal(n_samples) +
                 1j * rng.standard_normal(n_samples)) * np.sqrt(noise_power / 2)

        def psk_symbols(M):
            k    = int(np.log2(M))
            bits = rng.integers(0, 2, n_samples * k)
            idxs = np.packbits(bits.reshape(-1, k), axis=1,
                               bitorder='big').flatten()[:n_samples] % M
            angles = 2 * np.pi * idxs / M
            return np.exp(1j * angles)

        def qam_symbols(M):
            sqM   = int(np.sqrt(M))
            bits  = rng.integers(0, sqM, n_samples)
            bits2 = rng.integers(0, sqM, n_samples)
            levels = np.linspace(-(sqM - 1), sqM - 1, sqM)
            s = (levels[bits] + 1j * levels[bits2])
            return s / np.max(np.abs(s))

        if mod == "BPSK":
            sym = psk_symbols(2)
            sig = np.repeat(sym, max(1, n_samples // len(sym)))[:n_samples]
        elif mod == "QPSK":
            sym = psk_symbols(4)
            sig = np.repeat(sym, max(1, n_samples // len(sym)))[:n_samples]
        elif mod == "8PSK":
            sym = psk_symbols(8)
            sig = np.repeat(sym, max(1, n_samples // len(sym)))[:n_samples]
        elif mod == "16QAM":
            sym = qam_symbols(16)
            sig = np.repeat(sym, max(1, n_samples // len(sym)))[:n_samples]
        elif mod == "64QAM":
            sym = qam_symbols(64)
            sig = np.repeat(sym, max(1, n_samples // len(sym)))[:n_samples]
        elif mod == "RTCM/MSK":
            bits  = rng.integers(0, 2, n_samples) * 2 - 1
            phase = np.cumsum(bits * np.pi / (2 * max(1, n_samples // 256)))
            sig   = np.exp(1j * phase)
        elif mod == "GMSK":
            from scipy.ndimage import gaussian_filter1d
            bits  = (rng.integers(0, 2, n_samples) * 2 - 1).astype(float)
            bits  = gaussian_filter1d(bits, sigma=max(1, n_samples // 512))
            phase = np.cumsum(bits * np.pi / (2 * max(1, n_samples // 256)))
            sig   = np.exp(1j * phase)
        elif mod == "FSK":
            bits  = rng.integers(0, 2, n_samples)
            dev   = 0.25
            phase = np.cumsum((bits * 2 - 1) * 2 * np.pi * dev / fs * fs / n_samples * n_samples)
            sig   = np.exp(1j * phase / n_samples * 2 * np.pi)
        elif mod == "AM-DSB":
            msg = np.sin(2 * np.pi * 0.02 * np.arange(n_samples))
            sig = (1 + 0.8 * msg).astype(complex)
        elif mod == "FM":
            msg   = np.sin(2 * np.pi * 0.015 * np.arange(n_samples))
            phase = np.cumsum(msg) * 0.3
            sig   = np.exp(1j * phase)
        elif mod == "OFDM":
            n_sc = 64
            sym  = np.zeros(n_samples, dtype=complex)
            block = n_sc
            for start in range(0, n_samples - block, block):
                pilots = np.exp(1j * np.random.uniform(0, 2*np.pi, n_sc))
                sym[start:start+block] = np.fft.ifft(pilots)
            sig = sym
        elif mod == "CSS (LoRa-like)":
            bw  = 0.5
            sig = np.exp(1j * np.pi * bw * (np.arange(n_samples) / n_samples) ** 2 * n_samples)
        elif mod == "DSSS":
            data   = rng.integers(0, 2, n_samples // 11 + 1)
            chip   = rng.integers(0, 2, n_samples)
            spread = (np.repeat(data, 11)[:n_samples] ^ chip) * 2 - 1
            sig    = spread.astype(complex)
        elif mod == "CW":
            key = np.zeros(n_samples)
            for i in range(0, n_samples, n_samples // 8):
                key[i:i + n_samples // 16] = 1
            sig = key.astype(complex)
        else:
            sig = np.ones(n_samples, dtype=complex)

        sig = sig / (np.max(np.abs(sig)) + 1e-12)
        return sig + noise

    @staticmethod
    def spectral_shape(mod: str, freqs_norm: np.ndarray) -> np.ndarray:
        f = freqs_norm
        if mod in ("BPSK",):
            return np.sinc(f * 10) ** 2
        elif mod in ("QPSK", "8PSK"):
            return np.sinc(f * 20) ** 2
        elif mod in ("16QAM", "64QAM"):
            bw = 0.25
            return np.where(np.abs(f) < bw, 1.0,
                   np.where(np.abs(f) < bw * 1.5,
                            0.5 * (1 + np.cos(np.pi * (np.abs(f) - bw) / (0.5 * bw))),
                            0.0))
        elif mod in ("RTCM/MSK", "GMSK"):
            s = 0.5 * np.sinc((f - 0.25) * 8)**2 + 0.5 * np.sinc((f + 0.25) * 8)**2
            return s / (s.max() + 1e-12)
        elif mod == "FSK":
            s = 0.5 * np.exp(-((f-0.15)**2)/(2*0.04**2)) + 0.5 * np.exp(-((f+0.15)**2)/(2*0.04**2))
            return s / s.max()
        elif mod == "AM-DSB":
            s = 0.6*np.exp(-(f**2)/(2*0.01**2)) + 0.2*np.exp(-((f-0.05)**2)/(2*0.01**2)) + 0.2*np.exp(-((f+0.05)**2)/(2*0.01**2))
            return s / s.max()
        elif mod == "FM":
            bw = 0.2
            return np.where(np.abs(f) < bw, 1.0, np.exp(-((np.abs(f)-bw)**2)/(2*0.02**2)))
        elif mod == "OFDM":
            bw = 0.35
            return np.where(np.abs(f) < bw, 1.0, np.exp(-((np.abs(f)-bw)**2)/(2*0.015**2)))
        elif mod == "CSS (LoRa-like)":
            return np.where(np.abs(f) < 0.45, 1.0, 0.0).astype(float)
        elif mod == "DSSS":
            return np.where(np.abs(f) < 0.48, 0.9 + 0.1*np.random.rand(len(f)), 0.0)
        elif mod == "CW":
            return np.exp(-(f**2)/(2*0.005**2))
        else:
            return np.ones_like(f)



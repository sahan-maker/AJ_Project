# FHSS Engine
# ─────────────────────────────────────────────────────────────────────────────
class FHSSEngine:
    def __init__(self):
        self.num_channels  = 8
        self.base_freq     = 2400.0
        self.channel_bw    = 1.0
        self.hop_interval  = 10
        self.sequence_len  = 32
        self.seed          = 42
        self.pattern       = "Pseudo-Random"
        self.modulation    = "QPSK"
        self.snr_db        = 20.0
        self._build_channels()
        self._build_sequence()

    def _build_channels(self):
        self.channels = [
            {"id": i, "freq": round(self.base_freq + i * self.channel_bw, 2),
             "label": f"CH{i+1}", "color": CHANNEL_COLORS[i % len(CHANNEL_COLORS)]}
            for i in range(self.num_channels)
        ]

    def _build_sequence(self):
        rng = random.Random(self.seed)
        if self.pattern == "Pseudo-Random":
            self.sequence = [rng.randint(0, self.num_channels - 1) for _ in range(self.sequence_len)]
        elif self.pattern == "Sequential":
            self.sequence = [i % self.num_channels for i in range(self.sequence_len)]
        elif self.pattern == "Interleaved":
            self.sequence = [
                (i*2) % self.num_channels if i%2==0 else (i*2+1) % self.num_channels
                for i in range(self.sequence_len)
            ]
        else:
            self.sequence = [rng.randint(0, self.num_channels - 1) for _ in range(self.sequence_len)]

    def reconfigure(self, **kwargs):
        for k, v in kwargs.items():
            setattr(self, k, v)
        self._build_channels()
        self._build_sequence()

    @property
    def time_axis(self):
        return [i * self.hop_interval for i in range(self.sequence_len)]

    @property
    def freq_axis(self):
        return [self.channels[ch]["freq"] for ch in self.sequence]

    @property
    def total_bw(self):
        return self.num_channels * self.channel_bw

    @property
    def span_start(self):
        return self.base_freq - self.channel_bw * 0.5

    @property
    def span_end(self):
        return self.base_freq + self.num_channels * self.channel_bw + self.channel_bw * 0.5


